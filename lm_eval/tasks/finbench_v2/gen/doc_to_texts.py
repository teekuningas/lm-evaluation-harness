def render_template_with_instructions(doc, template_str):
    """
    Render a Jinja2 template with doc data and add instructions.
    
    For MCF templates, choices are already included inline in the template,
    so we only add instructions at the beginning.
    
    Args:
        doc: Document dict from dataset
        template_str: Jinja2 template string for the question
        
    Returns:
        Complete prompt with instructions and question (including inline choices)
    """
    prompt_parts = []
    
    # Add critical instructions
    instructions = (
        "TÄRKEÄÄ: Vastaa lyhyesti ja selkeästi. "
        "Valitse täsmälleen yksi vastausvaihtoehto annetuista. "
        "Vastaa VAIN valintavaihtoehdolla, ÄLÄ numerolla. "
        "Älä anna pitkiä selityksiä. "
        "Vastauksesi luetaan automaattisesti.\n\n"
    )
    prompt_parts.append(instructions)
    
    # Render the template with doc data
    try:
        template = Template(template_str)
        question_text = template.render(**doc)
        prompt_parts.append(question_text)
    except Exception as e:
        # If template rendering fails, fall back to basic extraction
        if "query" in doc:
            prompt_parts.append(f"{doc['query']}\n")
        elif "question" in doc:
            prompt_parts.append(f"{doc['question']}\n")
        elif "text" in doc:
            prompt_parts.append(f"{doc['text']}\n")
        elif "inputs" in doc:
            prompt_parts.append(f"{doc['inputs']}\n")
        
        # Add choices if template failed and they exist
        choices = utils.get_choices_from_doc(doc, debug=False)
        if choices:
            prompt_parts.append("\nVaihtoehdot:\n")
            for i, choice in enumerate(choices):
                prompt_parts.append(f"{i}. {choice}\n")
    
    return "".join(prompt_parts)


